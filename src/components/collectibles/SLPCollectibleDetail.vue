<template>
  <q-dialog v-model="val" full-width persistent seamless>
    <q-card v-if="collectible" style="max-width:90vw;" :class="{'pt-dark': darkMode}">
      <q-card-section class="row no-wrap items-start" :class="[darkMode ? 'pt-dark-label' : 'pp-text']">
        <div class="text-h6" :class=" $store.getters['darkmode/getStatus'] ? 'text-grad' : 'text-black'">{{ collectible.name }}</div>
        <q-space/>
        <q-btn icon="close" flat round dense v-close-popup />
      </q-card-section>
      <template v-if="imageUrl">
        <q-img :src="imageUrl" fit="fill" width="75"></q-img>
      </template>
      <template v-else>
        <gravatar :hash="collectible && collectible.token_id"/>
      </template>
      <q-card-section style="text-align: center; margin-bottom: 10px;">
        <q-btn-group push style="color: rgb(60, 100, 246) !important;">
          <q-btn @click="verify" push :label="$t('Verify')" icon="visibility" />
          <q-btn @click="send" push :label="$t('Send')" icon="send" />
        </q-btn-group>
      </q-card-section>
    </q-card>
  </q-dialog>
</template>

<script>
import Gravatar from 'vue-gravatar'
import { openURL } from 'quasar'

export default {
  name: 'collectible',
  components: { Gravatar },
  props: {
    modelValue: {
      type: Boolean,
      default: false
    },
    collectible: { }
  },
  data () {
    return {
      val: this.modelValue,
      darkMode: this.$store.getters['darkmode/getStatus']
    }
  },
  computed: {
    imageUrl () {
      if (!this.collectible) return ''
      return this.collectible.thumbnail_image_url ||
            this.collectible.thumbnail_image_url ||
            this.collectible.original_image_url
    }
  },
  methods: {
    verify () {
      const url = 'https://simpleledger.info/#token/' + this.collectible.token_id
      openURL(url)
    },
    getImageUrl (collectible) {
      if (!collectible) return ''
      return collectible.thumbnail_image_url ||
            collectible.thumbnail_image_url ||
            collectible.original_image_url
    },
    send () {
      this.$router.push({
        name: 'transaction-send',
        query: {
          assetId: 'slp/' + this.collectible.token_id,
          tokenType: 65,
          amount: 1,
          symbol: this.collectible.symbol,
          image: this.getImageUrl(this.collectible)
        }
      })
    }
  },
  watch: {
    val () {
      this.$emit('update:modelValue', this.val)
    },
    modelValue () {
      this.val = this.modelValue
    }
  }
}
</script>
